def group_team_members(queryset):
    """گروه‌بندی اعضای فعال تیم برای نمایش در صفحه درباره ما."""
    buckets = OrderedDict((code, []) for code in TEAM_GROUP_ORDER)
    for member in queryset:
        code = member.member_group or TeamMember.GROUP_CONSULTANT
        if code not in buckets:
            code = TeamMember.GROUP_CONSULTANT
        buckets[code].append(member)

    labels = dict(TEAM_GROUP_CHOICES)
    return [
        {"code": code, "label": labels[code], "members": members}
        for code, members in buckets.items()
        if members
    ]

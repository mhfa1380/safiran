def add(
    category: str,
    cluster: str,
    title: str,
    slug: str,
    meta: str,
    keywords: str,
    image_brief: str,
) -> None:
    articles.append(
        {
            "id": len(articles) + 1,
            "category": category,
            "cluster": cluster,
            "title": title,
            "slug": slug,
            "meta_description": meta[:160],
            "keywords": keywords,
            "image_brief": image_brief,
            "status": "planned",
            "target_words": "1500-3000",
        }
    )


